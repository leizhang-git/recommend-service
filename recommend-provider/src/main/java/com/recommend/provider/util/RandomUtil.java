    /**
     * 随机产生begin-end之间的整数，包含二者！
     * @param begin
     * @param end
     * @return
     */
    public static int getNum(Integer begin, Integer end) {
        if(end > 0 && begin >= 0 && end > begin) {
            return new Random().nextInt(end - begin + 1) + begin;
        }
        return 0;
    }

